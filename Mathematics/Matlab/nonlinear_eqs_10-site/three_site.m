function F = three_site(xs)
% 
x = xs(1:3);
y = [0 xs(4:end)];
disp(x)
disp(y)

F(1) = 0.4*(1 - x(1)) - x(1)* (1 - x(2)) - x(1) * x(2) *(1 - y(2));
F(2) = x(1)*(1 - x(2)) - x(2)*(1 - x(3)) - x(2)*x(3)*(1 - y(3));
F(3) = x(2)*(1 - x(3)) - 1*x(3) + y(2)*(1 - x(3));
F(4) = x(1)*x(2)*(1 - y(2)) - y(2)*(1 - x(3)) - y(2)*(1 - y(3))*x(3);
F(5) = x(2)*x(3)*(1 - y(3)) + x(3)*y(2)*(1 - y(3)) - 1*y(3);
end

